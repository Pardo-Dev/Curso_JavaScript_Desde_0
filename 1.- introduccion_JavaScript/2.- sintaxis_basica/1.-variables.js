
/* Variables */

// Hay 3 tipos de variables:
// var --> Declaración de variables de alcance global.
var a = "Uso de var";
console.log(a)

// let --> Declaración de variables de alcance a nivel de bloque.
let b = "Uso de let";
console.log(b)

// const --> Declaración de variables no mutables.
let c = "Uso de const";
console.log(c)

// Declarar variables multiples
let d,e,f;
console.log(d,e,f)

// Inicializar variables
let i = 0;
console.log(i)